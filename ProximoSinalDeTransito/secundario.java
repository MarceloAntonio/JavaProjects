public class secundario {

int switchh(int a) {
    switch (a) {
        case 1:
            System.out.println("o proximo é verde");
            break;
        case 2:
            System.out.println("o proximo é amarelo");
            break;
        case 3:
            System.out.println("o proximo é vermelho");
            break;
        default:
            System.out.println("numero invalido");

            return a;
    }
    return a;
}

}
